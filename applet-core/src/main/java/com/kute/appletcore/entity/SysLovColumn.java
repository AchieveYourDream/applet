package com.kute.appletcore.entity;

public class SysLovColumn {
    private String lovCode;

    private String columnName;

    private String columnTitle;

    private String displayFlag;

    private String returnValueFlag;

    private String returnTextFlag;

    private String display;

    public String getLovCode() {
        return lovCode;
    }

    public void setLovCode(String lovCode) {
        this.lovCode = lovCode;
    }

    public String getColumnName() {
        return columnName;
    }

    public void setColumnName(String columnName) {
        this.columnName = columnName;
    }

    public String getColumnTitle() {
        return columnTitle;
    }

    public void setColumnTitle(String columnTitle) {
        this.columnTitle = columnTitle;
    }

    public String getDisplayFlag() {
        return displayFlag;
    }

    public void setDisplayFlag(String displayFlag) {
        this.displayFlag = displayFlag;
    }

    public String getReturnValueFlag() {
        return returnValueFlag;
    }

    public void setReturnValueFlag(String returnValueFlag) {
        this.returnValueFlag = returnValueFlag;
    }

    public String getReturnTextFlag() {
        return returnTextFlag;
    }

    public void setReturnTextFlag(String returnTextFlag) {
        this.returnTextFlag = returnTextFlag;
    }

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }
}