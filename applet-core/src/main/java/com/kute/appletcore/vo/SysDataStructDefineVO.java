package com.kute.appletcore.vo;

public class SysDataStructDefineVO {

    private String structCode;
    private String structName;
    private String nodeCodeRule;
    private String rulePrefix;
    private String ruleLength;
    private String description;

    private String nodeCodeRuleName;

    public String getStructCode() {
        return structCode;
    }

    public void setStructCode(String structCode) {
        this.structCode = structCode;
    }

    public String getStructName() {
        return structName;
    }

    public void setStructName(String structName) {
        this.structName = structName;
    }

    public String getNodeCodeRule() {
        return nodeCodeRule;
    }

    public void setNodeCodeRule(String nodeCodeRule) {
        this.nodeCodeRule = nodeCodeRule;
    }

    public String getRulePrefix() {
        return rulePrefix;
    }

    public void setRulePrefix(String rulePrefix) {
        this.rulePrefix = rulePrefix;
    }

    public String getRuleLength() {
        return ruleLength;
    }

    public void setRuleLength(String ruleLength) {
        this.ruleLength = ruleLength;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNodeCodeRuleName() {
        return nodeCodeRuleName;
    }

    public void setNodeCodeRuleName(String nodeCodeRuleName) {
        this.nodeCodeRuleName = nodeCodeRuleName;
    }
}